vim.opt.guicursor = "n-v-c-i:block"

-- set line break
vim.opt.lbr = true

-- set no signcolumn
vim.opt.signcolumn = "yes:1"

vim.opt.timeoutlen = 500

vim.opt.updatetime = 500

vim.opt.mouse = "nvic"

vim.opt.cinoptions = "(s,m1,g0"
